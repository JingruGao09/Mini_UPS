CREATE TABLE WORLD(
WORLD_ID SERIAL PRIMARY KEY NOT NULL,
);

CREATE TABLE TRUCK(
PACKAGE_ID INT REFERENCES PACKAGE(PACKAGE_ID),
WORLD_ID INT REFERENCES WORLD(WORLD_ID),
TRUCK_ID INT NOT NULL,
TRUCK_STATUS VARCHAR(20) NOT NULL DEFAULT 'TRAVELING',
PRIMARY KEY(PACKAGE_ID)
);

CREATE TABLE PACKAGE(
PACKAGE_ID INT PRIMARY KEY NOT NULL,
USER_ID INT REFERENCES PROFILE(USER_ID),
WORLD_ID INT REFERENCES WORLD(WORLD_ID),
WH_ID INT NOT NULL,
SEQNUM INT NOT NULL,
TRUCK_ID INT REFERENCES TRUCK(TRUCK_ID)
X INT NOT NULL,
Y INT NOT NULL
);

CREATE TABLE SHIPMENT(
SHIP_ID SERIAL PRIMARY KEY NOT NULL,
PACKAGE_ID INT REFERENCES PACKAGE(PACKAGE_ID),
STATUS VARCHAR(50) NOT NULL DEFAULT 'CREATED'
);

